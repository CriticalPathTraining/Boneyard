    public static Customer GetRandomCustomer() {

      string Gender = "";
      string FirstName = "";

      if (WingtipRandom.Next(1, 100) > 40) {
        Gender = "F";
        FirstName = GetRandomFemaleFirstName();
      }
      else {
        Gender = "M";
        FirstName = GetRandomMaleFirstName();
      }

      string LastName = GetRandomLastName();
      string Company = GetRandomCompany();
      string EmailAddress = (FirstName + "." + LastName + "@" + Company + ".com").Replace(" ", "").Replace("'", "");

      CustomerLocation customerLocation = GetRandomCustomerLocation();

      string Address = GetRandomAddress();
      string City = customerLocation.City;
      string State = customerLocation.State;
      string ZipCode = customerLocation.ZipCode;

      string AreaCode = customerLocation.AreaCode;

      string WorkPhoneNumber = GetRandomPhoneNumber(AreaCode);
      string HomePhoneNumber = GetRandomPhoneNumber(AreaCode);

      DateTime BirthDate = GetRandomBirthDate();

      if (State.Equals("FL")) {
        BirthDate = BirthDate.AddYears(-21);
      }

      if (Gender.Equals("F")) {
        BirthDate = BirthDate.AddYears(-4);
      }

      DateTime FirstPurchaseDate = DateTime.Today;
      DateTime LastPurchaseDate = DateTime.Today;

      List<InvoiceDetail> invoiceDetails = new List<InvoiceDetail>();

      int ItemCount = WingtipRandom.Next(1, 3);

      for (int index = 0; index < ItemCount; index++) {

        int productId = WingtipRandom.Next(1, Products.Length);
        Product product = Products[productId];
        int quantity = WingtipRandom.Next(1, 10);
        double price = quantity * product.ListPrice;
        invoiceDetails.Add(new InvoiceDetail { ProductId = productId, Quantity = quantity, Price = price });

      }

      int invoiceTypePicker = WingtipRandom.Next(1, 100);
      string invoiceType = "Online";
      if (invoiceTypePicker < 25) {
        invoiceType = "InPerson";
      }
      if (invoiceTypePicker > 95) {
        invoiceType = "MailOrder";
      }

        
      Invoice FirstInvoice = new Invoice {
        InvoiceDate = FirstPurchaseDate,
        InvoiceAmount = Convert.ToDouble(WingtipRandom.Next(695, 12000) / 100),
        InvoiceType = invoiceType,
        InvoiceDetails = invoiceDetails
      };

      return new Customer {
        FirstName = FirstName,
        LastName = LastName,
        Company = Company,
        EmailAddress = EmailAddress,
        WorkPhone = WorkPhoneNumber,
        HomePhone = HomePhoneNumber,
        Address = Address,
        City = City,
        State = State,
        ZipCode = ZipCode,
        Gender = Gender,
        BirthDate = BirthDate,
        FirstPurchaseDate = FirstPurchaseDate,
        LastPurchaseDate = LastPurchaseDate,
        FirstInvoice = FirstInvoice
      };
    }

